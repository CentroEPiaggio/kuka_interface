function r = smooth_function(a)
%{
===========================================================================
	This is the smooth function for constraint activation, i.e. a fifth 
    order polynomial here


---------------------------------------------------------------------------
INPUT
---------------------------------------------------------------------------

    a       	[1 x 1]                	real number

---------------------------------------------------------------------------
OUTPUT
---------------------------------------------------------------------------

	r           [1 x 1]                 polynomial value

===========================================================================
%}

    r = 6*a^5 - 15*a^4 + 10*a^3;
    
end